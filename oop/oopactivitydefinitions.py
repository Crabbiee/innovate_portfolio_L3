# - inheritance - defining a class that inherits the properties of another class (parent>child)

# - encapsulation - keeping data and functions that work on the data together. can be used to prevent data from being modified by accident or used by those who do not need to use it.

# - polymorphism - when something can be used in different scenarios for different results. e.g "+"" has different usages on a string and an integer

# - abstraction - hiding unneccesary information from the user to reduce complexity/simplify. can be used to allow the user to perform an action with a simple UI without having to worry about the backend processes.

